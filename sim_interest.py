p=int(input("enter the principle amount : "))
r=int(input("enter the interest rate : "))
t=int(input("enter the time period : "))
s=(p*r*t)/100
print("the simple interest of {0} at {1} time period is {2}.".format(p,t,s))
'''
simple intrest formula is,

(principle amount + interest rate + time period) / 100

'''
